            <h2>How Trackman Works</h2>
            <p>It’s basically microwaves reflecting from the movement of a golf club head and the resulting ball flight. The change in frequency of these receiving waves is what makes it possible for us to track what happens at the very moment of impact between club and ball.
                TrackMan has a long track record of working with radar technology equipment and was established in 2003 with the sole purpose of helping golf professionals understand more about this critical moment of impact. The TrackMan radar not only helps map these key data parameters - golf swing and ball speed, attack angle, club and path, etc. - it also captures the golf swing with an inbuilt video camera.
                Radar technology (also called a portable golf launch monitor or a swing analyzer) is an integral part of golf instruction today, whether it’s used for golf swing lessons, analysis or club fitting. The radar is suited to be used by professional trainers, coaches and players as well as golf beginners, as the software app offers games and golf swing drills.

            <p>The radar can be used outdoors as well as indoor in a golf simulator environment. You can learn more about all these principles at TrackMan University, which offers an abundance of free learning sessions, from gamification tools to golf tips, drills and quizzes.</p></p>
